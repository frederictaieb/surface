    <p>
    Non unifié, les self Managers deviennent Ultra Manager, les Explorateurs deviennent Pompiers et les Enfants deviennent Exilés</li>
    </p>

    <ul>
        <li>Les Ultra managers : <strong>"plus jamais ça"</strong>. Controle, rigidité, pression, répression, Inhibition, procrastination, froideur, indifférence, superficialité, évitement, auto-critique, critique des autres, exigence, perfectionnisme, sentiment d'urgence, porter des masques, repli sur soi, se rendre invisible, auto-sabotage, se sur-adapter, se sacrifier pour les autres, doute de soi, des autres et de la vie, calculer tous les risques avant d'agir.</li>
        <li>Les pompiers viennent après la catastrophe pour éteindre le feu, quitte à couper le lien à la sensation:"tout va bien, je ne ressens rien". C'est un distraction, une fuite du ressenti: <strong>"Eteindre la douleur, coute que coute"</strong>.</li>
        <li>Les exilés sont ceux qui crient <strong>"Au secours"</strong>: Peur, Angoisse, Panique, Chagrin, Peine, Solitude, Doute, Détresse, Honte, Désespoir. Dans la tristesse, la détresse d'avoirvécu cela sans être accueillir, reconnu, restauré.</li>
    </ul>

    <h3>Cercle de partage</h3>

    <p>Préserver et croître deviendrait peut-être Préserver et changer, car le changement ne garantit pas la croissance. Les parts qui préservent aiment le connu. C'est ce qui les font se sentir en sécurité. 
    Plus il y a de Self et plus il y a de sécurité. L'exploration dépend donc de la capicité à développer du self</p>


    <h2>02/07/23</h2>
    <h3>Cercle de partage</h3>
    <p>
        J'ai parlé de la part de moi qui n'a plus envie de bouger, comme un manager devenu SDF. Il a été révélé que c'était plutot une part exhilée. Il y aurait d'abord à accueillir la part exploratrice qui veut à tout prix qu'on réalise des choses avec un il faut que ca bouge. Le fardeau de l'exhilé, désespéré: accueillir le Fardeau. La croyance que c'est cause perdue. L'exilé contamine le système. Et aussi une part qui dit: "il faut l'accueillir", mais qui perd patience et voudrait la jeter par dessus bord.
    </p>
    <p>
        Le Self qui prend soin de celle qui voudrait que ca bouge pour l'innonder de douceur et alors l'exhilé peut s'ouvrir, sortir de la honte et lacher son fardeau.
    </p>

    <h3>La Guidance a partir du Self</h3>

    <p>
        Le self a une sagesse qui permet d'offrir une guidance au système. Débloquer cette part, distinguer sa voix à elle est un grand pas. Offre un important éclairage.
    </p>

    <p>La voix du Self est souvent tenue, et plus on l'écoute et plus elle se manifeste.</p>


    <h3>Cadre de Pratique</h3>
    <ul>
        <li><strong>Vigilance bienveillante et curieuse:</strong> Sentir le critique intérieur</li>
        <li><strong>Le U Turn:</strong> Un évènement a lieu à l'exterieur, une intéraction et j'observe ce que cela fait en moi. Je tourne mon attention vers où cela réagit en moi, qui a besoin dêtre accueilli et les noter</li>
        <li><strong>Parler pour nos parts</strong> plutôt que depuis nos parts, cela permet d'inviter plus de self dans nos interactions de groupe. Se décoller, se désidentifier. Avec quelle intention je parle de ma part?</li>
        <li><strong>Liberté de faire</strong> ou de ne pas faire, tout en informant</li>
    </ul>

    <h3>La Danse des Parts</h3>
    <p>
        Pendant cette danse des parts, j'ai pensé à Anna, au fait que je ne l'ai pas aidé à prendre soin de son Palmier; cette idée que je ne l'ai pas aidé dans une dynamique pour prendre soin de sa plante, et qu'au lieu de ca, j'ai ramené l'attention à mon envie qu'on se connecte par le corps. A partir de la, je me suis demandé si ma relation était elle que je pourrais donner ma vie pour elle. S'en est suivi des réflexions sur la guerre et la perte d'un être cher.
    </p>

